namespace L0063 {
    class Solution {
    public:
        int uniquePathsWithObstacles(vector<vector<int>> &obstacleGrid) {
            int num_row = obstacleGrid.size();
            if (num_row == 0) return 0;
            int num_col = obstacleGrid[0].size();
            if (num_col == 0) return 0;

            vector<size_t> counter(num_col, 1);
            vector<int> &current_row = obstacleGrid[0];
            if (current_row[0] == 1) return 0;
            if (obstacleGrid[num_row - 1][num_col - 1]) return 0;
            if (num_row == 1 || num_col == 1) return 1;
            for (int i = 1; i < num_col; i++) {
                if (current_row[i]) {
                    for (int j = i; j < num_col; j++) { counter[j] = 0; }
                    break;
                }
            }

            for (int row = 1; row < num_row; row++) {
                current_row = obstacleGrid[row];
                if (current_row[0]) counter[0] = 0;

                for (int col = 1; col < num_col; col++) {
                    if (current_row[col]) counter[col] = 0; // self is blocked
                    else counter[col] = counter[col - 1] + counter[col];
                }
            }

            return counter.back();
        }
    };

}

